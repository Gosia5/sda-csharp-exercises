using System;

namespace sda_csharp_exercises
{
    class Program
    {
        static void Hasło(string[] args)
        {
            string hasło = Console.ReadLine();
            int długość = hasło.Length;
            {
                if(długość < 6)
                { Console.WriteLine("Za mało liter."); }
                if (długość = !char.IsUpperUpper)
                { Console.WriteLine("Brak dużej litery."); }
                if (długość = !int)
                { Console.WriteLine("Brak cyfry."); }
                if (!długość & *$#)
                { Console.WriteLine("Brak znaku specjalnego"); }
                else
                { Console.WriteLine("Hasło poprawne"); }
            }
        }
    }
}
